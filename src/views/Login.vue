<template>
  <div class="login-wrap">
    <ChooseLang/>
    <div class="ms-login">
      <div class="ms-title">{{ $t("login.title") }}</div>
      <el-form :model="ruleForm" ref="ruleForm" label-width="0px" class="ms-content">
        <el-form-item prop="username">
          <el-input v-model="ruleForm.username" :placeholder="$t('login.placeholderUsername')"></el-input>
        </el-form-item>
        <el-form-item prop="password">
          <el-input
            type="password"
            :placeholder="$t('login.placeholderPwd')"
            v-model="ruleForm.password"
            @keyup.enter.native="submitForm()"
          ></el-input>
        </el-form-item>
        <div class="login-btn">
          <el-button type="primary" @click="submitForm()">{{ $t("login.submit") }}</el-button>
        </div>
      </el-form>
    </div>
  </div>
</template>

<script lang="ts">
import axios from "axios";
import { Component, Vue } from "vue-property-decorator";
import ChooseLang from "@/components/ChooseLang.vue";
@Component({
  components: {
    ChooseLang
  }
})
export default class Login extends Vue {
  // data
  public ruleForm = {
    username: "",
    password: ""
  };
  public code = 0;
  //   钩子
  beforeCreate() {
    if (window.localStorage.getItem("username")) {
      this.$router.replace("/home");
    }
  }
  // computed
  // methods
  public submitForm() {
    if (this.ruleForm.username === "" || this.ruleForm.password === "") {
      // tslint:disable-next-line:label-position
      //  myThis: any = this
      this.$message({
        message: `${this.$t("login.rules")}`,
        type: "error"
      });
    } else {
      const url = "api/auth/login";
      const getPromise = axios.post(url, this.ruleForm);
      getPromise.then(resp => {
        // tslint:disable-next-line:no-console
        // console.log(resp);
        this.code = 200
        if (resp.data.code === "200") {
          this.$message({
            message: `${this.$t("login.success")}`,
            type: "success",
            duration: 1000
          });
          window.localStorage.setItem("username", this.ruleForm.username);
          window.localStorage.setItem("token", resp.data.msg);
          this.$router.replace("/home");
        } else {
          this.$message({
            showClose: true,
            message: `${this.$t("login.error")}`,
            type: "error"
          });
        }
      });
      getPromise.catch(err => {
        // tslint:disable-next-line:no-console
        console.log(err);
      });
      return getPromise;
    }
  }
}
</script>
